package org.elpy.sip.module.context.impl;

import org.elpy.sip.module.context.ResponseContext;

/**
 * Response context class for web modules
 * if {@link #getRedirectUrl()} is not null, then {@link #getResponseBody()} must not be used
 * @author Alexander Akhtyamov
 */
public class DefaultResponseContext implements ResponseContext{
    private int statusCode;
    
    private String statusMessage;
    
    private String redirectUrl;
    
    private byte[] responseBody;

    public int getStatusCode() {
        return statusCode;
    }

    public void setStatusCode(int statusCode) {
        this.statusCode = statusCode;
    }

    public String getStatusMessage() {
        return statusMessage;
    }

    public void setStatusMessage(String statusMessage) {
        this.statusMessage = statusMessage;
    }

    public String getRedirectUrl() {
        return redirectUrl;
    }

    public void setRedirectUrl(String redirectUrl) {
        this.redirectUrl = redirectUrl;
    }

    public byte[] getResponseBody() {
        return responseBody;
    }

    public void setResponseBody(byte[] responseBody) {
        this.responseBody = responseBody;
    }
}
